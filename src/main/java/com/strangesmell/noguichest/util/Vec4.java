package com.strangesmell.noguichest.util;

public class Vec4 {
    float a1;
    float a2;
    float a3;
    float a4;

    public Vec4(float a1, float a2, float a3, float a4) {
        this.a1 = a1;
        this.a2 = a2;
        this.a3 = a3;
        this.a4 = a4;
    }


    public float getA1() {
        return a1;
    }

    public float getA2() {
        return a2;
    }

    public float getA3() {
        return a3;
    }

    public float getA4() {
        return a4;
    }
}
